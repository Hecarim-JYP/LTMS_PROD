-- 역할-권한 매핑 테이블 (Many-to-Many)
CREATE TABLE `role_permission` (
  `role_permission_id` INT AUTO_INCREMENT COMMENT 'PK',
  `company_id` INT(11) NOT NULL DEFAULT 1 COMMENT '회사 ID (외래키)',
  `role_id` INT(11) NOT NULL COMMENT '역할 ID (외래키)',
  `permission_id` INT(11) NOT NULL COMMENT '권한 ID (외래키)',
  `assigned_by` INT(11) DEFAULT NULL COMMENT '할당자 ID (외래키)',
  `assigned_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP() COMMENT '할당 일시',
  `is_active` TINYINT(1) DEFAULT 1 COMMENT '활성 여부 (1: 활성, 0: 비활성)',
  `sort_order` INT(11) DEFAULT 1 COMMENT '정렬 순서',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP() COMMENT '생성일시',
  `created_by` INT(11) DEFAULT NULL COMMENT '생성자 ID (외래키)',
  `updated_at` DATETIME DEFAULT CURRENT_TIMESTAMP() ON UPDATE CURRENT_TIMESTAMP() COMMENT '수정일시',
  `updated_by` INT(11) DEFAULT NULL COMMENT '수정자 ID (외래키)',
  `deleted_at` DATETIME DEFAULT NULL COMMENT '삭제일시 (Soft Delete)',
  `deleted_by` INT(11) DEFAULT NULL COMMENT '삭제자 ID (외래키)',
  PRIMARY KEY (`role_permission_id`),
  UNIQUE KEY `uk_role_permission` (`role_id`, `permission_id`, `deleted_at`) COMMENT '역할-권한 고유 제약',
  KEY `idx_role_id` (`role_id`) COMMENT '역할별 인덱스',
  KEY `idx_permission_id` (`permission_id`) COMMENT '권한별 인덱스',
  KEY `idx_company_id` (`company_id`) COMMENT '회사별 인덱스',
  KEY `idx_is_active` (`is_active`) COMMENT '활성 여부 인덱스',
  KEY `idx_role_active` (`role_id`, `is_active`, `deleted_at`) COMMENT '역할의 활성 권한 조회',
  KEY `idx_permission_active` (`permission_id`, `is_active`, `deleted_at`) COMMENT '권한의 활성 역할 조회',
  KEY `idx_company_role_permission` (`company_id`, `role_id`, `permission_id`, `is_active`) COMMENT '회사별 역할-권한 조회',
  KEY `idx_company_active_sort` (`company_id`, `is_active`, `sort_order`) COMMENT '회사, 활성 여부 및 정렬 순번 인덱스'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='역할-권한 매핑 테이블 - RBAC 패턴';

-- 역할-권한 매핑 데이터 삽입
-- role_id: 1=ADMIN, 2=MANAGER, 3=USER, 4=GUEST
-- permission_id는 permission 테이블에서 자동으로 조회 (총 71개)

-- ========================================
-- ADMIN (role_id=1) - 모든 권한 부여
-- ========================================
-- 모든 모듈의 모든 액션 권한 (read, create, update, delete, approval)
INSERT INTO `role_permission` (`company_id`, `role_id`, `permission_id`, `is_active`, `sort_order`, `created_by`)
SELECT 1, 1, `permission_id`, 1, `sort_order`, NULL
FROM `permission`
WHERE `company_id` = 1 AND `deleted_at` IS NULL;

-- ========================================
-- MANAGER (role_id=2) - 관리자 권한 (delete 제외)
-- ========================================
-- 생성, 조회, 수정, 결재 권한 부여 (삭제 권한 제외)
INSERT INTO `role_permission` (`company_id`, `role_id`, `permission_id`, `is_active`, `sort_order`, `created_by`)
SELECT 1, 2, `permission_id`, 1, `sort_order`, NULL
FROM `permission`
WHERE `company_id` = 1 
  AND `deleted_at` IS NULL
  AND `action` IN ('create', 'read', 'update', 'approval');

-- ========================================
-- USER (role_id=3) - 일반 사용자 권한
-- ========================================
-- 생성, 조회 권한만 부여 (수정, 삭제, 결재 권한 없음)
INSERT INTO `role_permission` (`company_id`, `role_id`, `permission_id`, `is_active`, `sort_order`, `created_by`)
SELECT 1, 3, `permission_id`, 1, `sort_order`, NULL
FROM `permission`
WHERE `company_id` = 1 
  AND `deleted_at` IS NULL
  AND `action` IN ('read', 'create');

-- ========================================
-- GUEST (role_id=4) - 게스트 권한
-- ========================================
-- 조회 권한만 부여
INSERT INTO `role_permission` (`company_id`, `role_id`, `permission_id`, `is_active`, `sort_order`, `created_by`)
SELECT 1, 4, `permission_id`, 1, `sort_order`, NULL
FROM `permission`
WHERE `company_id` = 1 
  AND `deleted_at` IS NULL
  AND `action` = 'read';





